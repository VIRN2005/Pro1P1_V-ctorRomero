    public int Binario_a_Decimal(String binario, int exponent, int num) {
        if (exponent == binario.length()) {
            return num;
        } else {
            return Binario_a_Decimal(binario, exponent + 1, num + (int) Math.pow((binario.charAt(binario.length() - 1 - exponent) - 48) * 2, exponent));
        }
    }

    public int Decimal_a_Binario(int numero) {
        String Binary = "";
        if (numero == 0) {
            return 0;
        } else {
            return (numero % 2 + 10 * Decimal_a_Binario(numero / 2));
        }
    }

        
